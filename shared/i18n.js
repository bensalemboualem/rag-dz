// ========== i18n CLASSE ==========
class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('iaf_language') || 'fr';
        this.translations = translations;
    }

    setLanguage(lang) {
        if (['fr', 'en', 'ar'].includes(lang)) {
            this.currentLang = lang;
            localStorage.setItem('iaf_language', lang);
            document.documentElement.lang = lang;

            // RTL pour l'arabe
            if (lang === 'ar') {
                document.documentElement.dir = 'rtl';
            } else {
                document.documentElement.dir = 'ltr';
            }

            // Émettre événement de changement de langue
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));

            return true;
        }
        return false;
    }

    getLanguage() {
        return this.currentLang;
    }

    t(path) {
        const keys = path.split('.');
        let value = this.translations;

        for (const key of keys) {
            value = value[key];
            if (!value) return path;
        }

        return value[this.currentLang] || value['fr'] || path;
    }

    // Helper pour traduire dynamiquement
    translate(element) {
        const key = element.getAttribute('data-i18n');
        if (key) {
            element.textContent = this.t(key);
        }
    }

    // Traduire toute la page
    translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => this.translate(el));
    }
}

// Instance globale
window.i18n = new I18n();

